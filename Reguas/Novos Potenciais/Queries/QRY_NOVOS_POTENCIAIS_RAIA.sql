SELECT
    CCL.ID_CLIENTE
    ,CCL.CARTAO
    ,CCL.EMAIL
    ,CCL.TIPO_CADASTRO
    ,CCL.FILIAL_CADASTRO
    ,CCL.DATA_CADASTRO
    ,CCL.CADASTRO_WEB
    ,CCL.TEL_CELULAR
    ,CCL.NOME
    ,NTH.DATA_HORA_VENDA
    ,NTH.VALOR_TOTAL_NF
    ,CONVERT(VARCHAR(10), GETDATE()+10,101) AS DT_OFERTA_TURBINADA
    ,CONVERT(VARCHAR(10), GETDATE()+17,101) AS DT_VOUCHER
    ,CONVERT(VARCHAR(10), GETDATE()+24,101) AS DT_VOUCHER_REPIQUE
    ,CASE
        WHEN NTH.VALOR_TOTAL_NF >= 50.00 AND NTH.VALOR_TOTAL_NF < 100.00 THEN 1
        WHEN NTH.VALOR_TOTAL_NF >= 100.00 AND NTH.VALOR_TOTAL_NF < 150.00 THEN 2
        WHEN NTH.VALOR_TOTAL_NF >= 150.00 AND NTH.VALOR_TOTAL_NF < 200.00 THEN 3
        WHEN NTH.VALOR_TOTAL_NF >= 200.00 THEN 4
        ELSE 0
    END AS FLAG_PRECO
    ,CASE
        WHEN NTH.VALOR_TOTAL_NF >= 50.00 AND NTH.VALOR_TOTAL_NF < 100.00 THEN '06000002200508'
        WHEN NTH.VALOR_TOTAL_NF >= 100.00 AND NTH.VALOR_TOTAL_NF < 150.00 THEN '06000002201008'
        WHEN NTH.VALOR_TOTAL_NF >= 150.00 AND NTH.VALOR_TOTAL_NF < 200.00 THEN '06000002201508'
        WHEN NTH.VALOR_TOTAL_NF >= 200.00 THEN '06000002202008'
        ELSE NULL
    END AS NR_PROMOCIONAL_BV
    ,CASE
        WHEN NTH.VALOR_TOTAL_NF >= 100.00 AND NTH.VALOR_TOTAL_NF < 150.00 THEN '06000002201208'
        WHEN NTH.VALOR_TOTAL_NF >= 150.00 AND NTH.VALOR_TOTAL_NF < 200.00 THEN '06000002201808'
        WHEN NTH.VALOR_TOTAL_NF >= 200.00 THEN '06000002202508'
        ELSE '06000002200708'
    END AS NR_PROMOCIONAL_OT
    ,'06000002101003' AS NR_PROMOCIONAL_VC
    ,'06000002101003' AS NR_PROMOCIONAL_VR
    ,CASE
        WHEN NTH.VALOR_TOTAL_NF >= 50.00 AND NTH.VALOR_TOTAL_NF < 100.00 THEN '217523'
        WHEN NTH.VALOR_TOTAL_NF >= 100.00 AND NTH.VALOR_TOTAL_NF < 150.00 THEN '217525'
        WHEN NTH.VALOR_TOTAL_NF >= 150.00 AND NTH.VALOR_TOTAL_NF < 200.00 THEN '217526'
        WHEN NTH.VALOR_TOTAL_NF >= 200.00 THEN '217527'
        ELSE ''
    END AS CD_CAMPANHA_OFERTA_RAIA_BV
    ,CASE
        WHEN NTH.VALOR_TOTAL_NF >= 100.00 AND NTH.VALOR_TOTAL_NF < 150.00 THEN '345835'
        WHEN NTH.VALOR_TOTAL_NF >= 150.00 AND NTH.VALOR_TOTAL_NF < 200.00 THEN '345836'
        WHEN NTH.VALOR_TOTAL_NF >= 200.00 THEN '345837'
        ELSE '345834'
    END AS CD_CAMPANHA_OFERTA_RAIA_OT
    ,'217528' AS CD_CAMPANHA_OFERTA_RAIA_VC
    ,'217528' AS CD_CAMPANHA_OFERTA_RAIA_VR
    ,'PT-BR' AS LOCALE
FROM DE_CAD_CLIENTE_RAIA CCL
LEFT JOIN DE_NOTA_HEADER_RAIA NTH
ON CCL.ID_CLIENTE = NTH.ID_CLIENTE
WHERE CONVERT(VARCHAR(10),CCL.DATA_CADASTRO, 101) = CONVERT(VARCHAR(10),GETDATE(), 101)
AND (CCL.EMAIL IS NOT NULL OR CCL.TEL_CELULAR IS NOT NULL)
AND CCL.CARTAO NOT LIKE '%Univers%' 
AND CCL.CARTAO NOT LIKE '%R.B.F.%'
AND NOT EXISTS
(
    SELECT 1
    FROM DE_ALL_NOVOS_POTENCIAIS_RAIA PTR
    WHERE PTR.ID_CLIENTE = CCL.ID_CLIENTE
)